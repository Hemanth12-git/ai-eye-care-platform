// ## Knowledge Base

// ### Common Eye Issues & Immediate Advice
// - **Eye strain**: Often from prolonged screen use. Rest eyes, adjust lighting, follow 20-20-20 rule.
// - **Dry eyes**: Use lubricating drops if appropriate, stay hydrated, reduce dry air exposure.
// - **Red eyes**: May indicate irritation, infection, or allergies. Avoid rubbing; seek care if painful or vision changes.
// - **Blurred vision**: Can have many causes. Stop driving if sudden; book an eye exam promptly.
// - **Eye injury**: Do not rub. Seek urgent care for chemical exposure, cuts, or foreign objects.

// ### When to Seek Immediate Care
// - Sudden vision loss or major vision change
// - Severe eye pain
// - Eye injury or chemical exposure
// - Flashes, floaters, or curtain-like vision loss
// - Significant redness with pain and light sensitivity

// ### Prevention Tips
// - Take regular screen breaks (20-20-20 rule)
// - Wear UV-protective sunglasses outdoors
// - Maintain a healthy diet rich in eye-supporting nutrients
// - Keep contact lenses clean and follow replacement schedules
// - Schedule regular comprehensive eye exams

